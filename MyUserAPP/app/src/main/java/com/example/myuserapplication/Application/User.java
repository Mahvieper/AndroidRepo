package com.example.myuserapplication.Application;

public class User {
    private String mName;
    private String mGender;
    private int mAge;
    private long mContact;
    private String mCountry;
    private String mState;
    private String mAadhaar;
    private String mIMEI;

    public String getmName() {
        return mName;
    }

    public void setmName(String mName) {
        this.mName = mName;
    }

    public String getmGender() {
        return mGender;
    }

    public void setmGender(String mGender) {
        this.mGender = mGender;
    }

    public int getmAge() {
        return mAge;
    }

    public void setmAge(int mAge) {
        this.mAge = mAge;
    }

    public long getmContact() {
        return mContact;
    }

    public void setmContact(long mContact) {
        this.mContact = mContact;
    }

    public String getmCountry() {
        return mCountry;
    }

    public void setmCountry(String mCountry) {
        this.mCountry = mCountry;
    }

    public String getmState() {
        return mState;
    }

    public void setmState(String mState) {
        this.mState = mState;
    }

    public String getmAadhaar() {
        return mAadhaar;
    }

    public void setmAadhaar(String mAadhaar) {
        this.mAadhaar = mAadhaar;
    }

    public String getmIMEI() {
        return mIMEI;
    }

    public void setmIMEI(String mIMEI) {
        this.mIMEI = mIMEI;
    }
}
